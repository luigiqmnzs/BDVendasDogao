    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    ProdutosDAO dao = new ProdutosDAO();
    Produtos p = null;
    if(tNome.getText().equalsIgnoreCase("") || tValorCompra.getText().equalsIgnoreCase("") || tValorVenda.getText().equalsIgnoreCase("") || tQuantidadeEstoque.getText().equalsIgnoreCase("") || tEstoqueMinimo.getText().equalsIgnoreCase("")){
        JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
    }else{
        try {
            if(dao.listar().isEmpty()){
                p = new Produtos(1, tNome.getText(), Double.parseDouble(tValorCompra.getText()), Double.parseDouble(tValorVenda.getText()), Integer.parseInt(tQuantidadeEstoque.getText()), Integer.parseInt(tEstoqueMinimo.getText()));
            }else{
                int cont = 1;
                for (Produtos produto : dao.listar()) {
                    if(produto.getCodigoProduto() > cont){
                        cont = produto.getCodigoProduto();
                    }
                }
                p = new Produtos(cont + 1, tNome.getText(), Double.parseDouble(tValorCompra.getText()), Double.parseDouble(tValorVenda.getText()), Integer.parseInt(tQuantidadeEstoque.getText()), Integer.parseInt(tEstoqueMinimo.getText()));
            }   } catch (SQLException ex) {
            Logger.getLogger(CadastrarProdutoUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    dao.inserir(p);
    JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");
    ProdutosUI ui = new ProdutosUI();
    ui.setVisible(true);
    this.setVisible(false);
    } 
    }//GEN-LAST:event_jButton1ActionPerformed

